<h1>
Death & Dessert 2010</h1>
	<img alt="" src="../images/2010death.JPG" width="479" height="260" /><br />
Authors: Mary Tatarian, Gerald Kelly and C.S. Challinor<p>The Friends of the Library of Bonita Springs hosted their annual &quot;Death and Dessert&quot; event 
with three local authors [pictured above] who spoke to an audience of approximately 35 participates.&nbsp;All three authors wrote mystery books . After 
they spoke their was a question and answer segment followed by the offerings of pastries and beverages. </p>
<p><img alt="" src="../images/2010dessert.JPG" width="220" height="165" style="float: right" />The authors, <strong>Mary Tatarian</strong> from Ft&nbsp;Myers 
Beach, <strong>Gerald Kelly</strong> from Bonita&nbsp;Springs and <strong>C.S. Challinor</strong> from Bonita Springs&nbsp;told what their books were about, how 
to get published and interesting facts about themselves plus much more.</p>
<p>Mary Tatarian&#39;s book, &quot;&nbsp;Seabliss&quot; is a cozy mystery crime solver by two women who are professionals. It is an adventurious, romantic beach book. 
She is presently working on her second book.</p>
<p>Gerald Kelly&#39;s book,&quot;Honor for Sale&quot; is the story of the &quot;French Connection&quot; case of the police corruption in the New York Police Dept.&nbsp; </p>
<p>C.S. Challinor has written four books and is presently working on her fifth. She writes Rex Graves mysteries which take place in exotic settings 
with diverse characters.&nbsp;</p>
<p>According to a member of the audience, Nancy Grube, when asked her opinion of the event,&nbsp;she said, &quot;I enjoyed listening to new authors because I 
find new books to read and it is always interesting to hear their background.&quot;</p>
<p>Look for news of our 2011 &quot;Death and Dessert&quot; and join us for an enjoyable and entertaining afternoon.</p>
<p class="style3">Spring 2010: Alys Morande</p>
